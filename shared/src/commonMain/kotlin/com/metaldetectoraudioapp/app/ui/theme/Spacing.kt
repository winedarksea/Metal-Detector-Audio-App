package com.metaldetectoraudioapp.app.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Shared spacing scale so padding and gaps are consistent across every screen and
 * platform, instead of hand-picked magic numbers. Keep this file byte-identical
 * with the Android copy (`app/.../ui/theme/Spacing.kt`) — the app module does not
 * depend on `:shared`, mirroring the Color/Theme duplication.
 */
object Spacing {
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 24.dp
}
